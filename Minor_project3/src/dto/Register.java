package dto;

public class Register {
private String firstname;
private String lastname;
private String counum;
private String mobile;
private String email;
private String address;
private String password;
private String aadhaarNum;
private String age;
public String getAadhaarNum() {
	return aadhaarNum;
}
public void setAadhaarNum(String aadhaarNum) {
	this.aadhaarNum = aadhaarNum;
}
public String getAge() {
	return age;
}
public void setAge(String age) {
	this.age = age;
}
public String getFirstname() {
	return firstname;
}
public void setFirstname(String firstname) {
	this.firstname = firstname;
}
public String getLastname() {
	return lastname;
}
public void setLastname(String lastname) {
	this.lastname = lastname;
}
public String getCounum() {
	return counum;
}
public void setCounum(String counum) {
	this.counum = counum;
}
public String getMobile() {
	return mobile;
}
public void setMobile(String mobile) {
	this.mobile = mobile;
}
public String getEmail() {
	return email;
}
public void setEmail(String email) {
	this.email = email;
}
public String getAddress() {
	return address;
}
public void setAddress(String address) {
	this.address = address;
}
public String getPassword() {
	return password;
}
public void setPassword(String password) {
	this.password = password;
}
}
